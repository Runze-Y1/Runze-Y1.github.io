$(document).ready(function() {
  $(".p3").mouseenter(function() {
    $(".p3").addClass("white");
    $(".p33").addClass("show white");
  });

  $(".p3").mouseleave(function() {
    $(".p3").removeClass("white");
    $(".p33").removeClass("show white");
  });
});



